package com.boost.lesson02;

public class Class_03_While {

	public static void main(String[] args) {

		int i = 0;
		while (i < 5) {
			i++;
			System.out.println("Hello, World!");

		} // loop ends

		i = 0;
		do {
			i++;
			System.out.println("Hello, World!");

		} while (i < 5);// önce işlemi yapıyor sonra kosula bakıyor.

	}

}
